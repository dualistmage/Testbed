#include "Encryptor.h"

DWORD Encryptor::roundKey_[48];
BYTE Encryptor::secretKey_[32] = {
    0x47, 0x06, 0x48, 0x08, 0x51, 0xe6, 0x1b, 0xe8, 0x5d, 0x74, 0xbf, 0xb3, 0xfd, 0x95, 0x61, 0x85,
    0xa7, 0x08, 0xc1, 0x28, 0x17, 0xe8, 0xcb, 0x45, 0x41, 0xa1, 0xb7, 0x15, 0xed, 0xa1, 0xb2, 0xa3 };


Encryptor::Encryptor()
{
    SeedRoundKey( roundKey_, secretKey_ );
}

bool Encryptor::encode( BYTE* data )
{
    SeedEncrypt( data, roundKey_ );
}

bool Encryptor::decode( BYTE* data )
{
    SeedDecrypt( data, roundKey_ );
}
